package demo;

public class Switch {

	public static void main(String[] args) 
	{
	int choice = 3;
	switch(choice)
	{
	case 1 :
	{
		System.out.println("case 1 is executing");
	}break;
	case 2 :
	{
		System.out.println("case 2 is executing");
	}break;
	case 3 :
	{
		System.out.println("case 3 is executing");
	}break;
	case 4 :
	{
		System.out.println("case 4 is executing");
	}break;
	default:
	{
		System.out.println("default is executing");
	}break;
	}
	

	}

}
